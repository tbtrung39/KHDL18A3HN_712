thu = int(input("Nhập thứ (1-7): "))
if thu == 1:
    print("Thứ đã nhập là: Sunday")
elif thu == 2:
    print("Thứ đã nhập là: Monday")
elif thu == 3:
    print("Thứ đã nhập là: Tuesday")
elif thu == 4:
    print("Thứ đã nhập là: Wednesday")
elif thu == 5:
    print("Thứ đã nhập là: Thursday")
elif thu == 6:
    print("Thứ đã nhập là: Friday")
elif thu == 7:
    print("Thứ đã nhập là: Saturday")
else:
    print("Thứ không hợp lệ!")
