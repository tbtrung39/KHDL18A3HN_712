thu = float(input("Nhập thứ (1-7): "))
if thu == 1:
    print("Chủ nhật")
elif thu == 2:
    print("Thứ hai")
elif thu == 3:
    print("Thứ ba")
elif thu == 4:
    print("Thứ tư")
elif thu == 5:
    print("Thứ năm")
elif thu == 6:
    print("Thứ sáu")
elif thu == 7:
    print("Thứ bảy")
else:
    print("Nhập sai vui lòng thử lại")
