thu = int(input("Nhập số thứ trong tuần (1-7): "))
if thu == 1:
    print("Thứ 1 là Chủ Nhật")
elif thu == 2:
    print("Thứ 2 là Thứ Hai")
elif thu == 3:
    print("Thứ 3 là Thứ Ba")
elif thu == 4:
    print("Thứ 4 là Thứ Tư")
elif thu == 5:
    print("Thứ 5 là Thứ Năm")
elif thu == 6:
    print("Thứ 6 là Thứ Sáu")
else:
    print("Thứ 7 là Thứ Bảy")
