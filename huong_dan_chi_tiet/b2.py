# câu 2: viết đoạn code thực hiện các phép tính số học
a = float(input("Nhập số thực a: "))
b = float(input("Nhập số thực b: "))
print(" Tổng của hai số thực a và b là : ", a + b)
print("Hiệu của hai số a và b là: ", a - b)
print("Tích của hai số a và b là: ", a * b)
print("Thương của hai số a và b là: ", a / b)
