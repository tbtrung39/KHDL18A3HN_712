# Viết chương trình nhập vào hai số nguyên dương m và n (m>n), hãy in ra màn hình phần nguyên và phần dư của m chia cho n
m = int(input("Nhập số nguyên dương m: "))
n = int(input("Nhập số nguyên dương n: "))
print(m,"chia cho",n,"có phần nguyên là", m//n)
print(m,"chia cho",n,"có phần dư là",m%n)
